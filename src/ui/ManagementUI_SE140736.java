    private void btnCateSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCateSaveActionPerformed
        if (!modelCal.validDate(txtCateID.getText().trim(),
                txtCateName.getText().trim(),
                txtCateDes.getText().trim())) {
            return;
        }
        if (addCategories) {
            try {
                if (!modelCal.validDateCodeCategories(txtCateID.getText().trim())) {
                    JOptionPane.showMessageDialog(this, "DUPLICATE CATEGORYID at " + txtCateID.getText().trim());
                    return;
                }
                Categories_SE140736 cate = new Categories_SE140736(txtCateID.getText().trim().toUpperCase(),
                        txtCateName.getText().trim(),
                        txtCateDes.getText().trim());

                if (modelCal.Insert(cate) == SUCCESS) {
                    modelCal.getModelCate().getList().add(cate);
                    modelCal.getListCategoriesComboBox().add(cate.getCategoryID() + "-" + cate.getCategoryName());
                }
                addCategories = false;
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            try {
                int selectedRow = tblCategories.getSelectedRow();
                if (selectedRow < 0) {
                    JOptionPane.showMessageDialog(this, "Please Choose Row to Edit!!");
                    return;
                }

                Categories_SE140736 cateNew = new Categories_SE140736(txtCateID.getText().toUpperCase().trim(),
                        txtCateName.getText().toUpperCase().trim(),
                        txtCateDes.getText().toUpperCase().trim());
                Categories_SE140736 cateOld = modelCal.getModelCate().getList().get(selectedRow);

                if (modelCal.Save(cateNew) == SUCCESS) {
                    modelCal.getListCategoriesComboBox().remove(cateOld.getCategoryID() + "-" + cateOld.getCategoryName());
                    cateOld.setCategoryName(cateNew.getCategoryName());
                    cateOld.setDescription(cateNew.getDescription());
                    modelCal.getListCategoriesComboBox().add(cateNew.getCategoryID() + "-" + cateNew.getCategoryName());
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        modelPro.getModelProducts().loadCategoriesComboBox();
        cbCategoryName.setModel(new DefaultComboBoxModel<>(modelCal.getListCategoriesComboBox()));
        tblCategories.updateUI();
    }//GEN-LAST:event_btnCateSaveActionPerformed

    private void btnCateDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCateDeleteActionPerformed
        try {
            int selectedRow = tblCategories.getSelectedRow();
            if (selectedRow < 0 || deleteCategories == false) {
                JOptionPane.showMessageDialog(this, "Choose the Row to Delete");
                return;
            }
            int r = JOptionPane.showConfirmDialog(this, "Do you want to Delete this Category?");
            if (r == JOptionPane.YES_OPTION) {
                Categories_SE140736 cate = modelCal.getModelCate().getList().get(selectedRow);

                if (!modelPro.checkConstrainCategoriesID(cate.getCategoryID())) {
                    JOptionPane.showMessageDialog(this, "Still Products is manager by this Category");
                    return;
                }
                if (modelCal.Delete(cate) == SUCCESS) { //xóa dlieu trong database trc
                    deleteCategories = false;
                    modelCal.getModelCate().getList().remove(cate);
                    modelCal.getListCategoriesComboBox().remove(cate.getCategoryID() + " - " + cate.getCategoryName());
                    JOptionPane.showMessageDialog(this, "Deleted");
                    cbCategoryName.setModel(new DefaultComboBoxModel<>(modelCal.getListCategoriesComboBox()));
                    clearInformationCategoriesOnTable();
                    modelPro.getModelProducts().loadCategoriesComboBox();
                    tblCategories.updateUI();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnCateDeleteActionPerformed

