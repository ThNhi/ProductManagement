    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        if (!modelPro.validDate(txtProductID.getText().trim(),
                txtProductName.getText().trim(),
                txtUnit.getText().trim(),
                txtPrice.getText().trim(),
                txtQuantity.getText().trim(),
                cbCategoryName.getSelectedItem())) {
            return;
        }
        if (addNewProduct) {
            try {
                if (!modelPro.validDateProductID(txtProductID.getText().trim())) {
                    JOptionPane.showMessageDialog(this, "DUPLICATED CODE at " + txtProductID.getText().trim());
                    return;
                }
                Products_SE140736 pro = new Products_SE140736(txtProductID.getText().toUpperCase().trim(),
                        txtProductName.getText().trim(),
                        txtUnit.getText().trim(),
                        Float.parseFloat(txtPrice.getText().trim()),
                        Integer.parseInt(txtQuantity.getText().trim()),
                        modelPro.loadCategoriesIntoProducts(cbCategoryName.getSelectedItem().toString()));

                if (modelPro.Insert(pro) == SUCCESS) {
                    modelPro.getModelProducts().getList().add(pro);
                }
                addNewProduct = false;
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            try {
                int selectedRow = tblProducts.getSelectedRow();
                if (selectedRow < 0) {
                    JOptionPane.showMessageDialog(this, "Please Choose Row to Edit");
                }

                Products_SE140736 proOld = modelPro.getModelProducts().getList().get(selectedRow);
                Products_SE140736 proNew = new Products_SE140736(txtProductID.getText().toUpperCase().trim(),
                        txtProductName.getText().trim(),
                        txtUnit.getText().trim(),
                        Float.parseFloat(txtPrice.getText().trim()),
                        Integer.parseInt(txtQuantity.getText().trim()),
                        modelPro.loadCategoriesIntoProducts(cbCategoryName.getSelectedItem().toString()));
                if (modelPro.Save(proNew) == SUCCESS) {
//                    proOld.setProductName(proNew.getProductName());
//                    proOld.setCategoryid(proNew.getCategoryid());
//                    proOld.setUnit(proNew.getUnit());
//                    proOld.setQuantity(proNew.getQuantity());
//                    proOld.setPrice(proNew.getPrice());
                    proOld.setProductName(proNew.getProductName());
                    proOld.setUnit(proNew.getUnit());
                    proOld.setPrice(proNew.getPrice());
                    proOld.setQuantity(proNew.getQuantity());
                    proOld.setCategoryid(proNew.getCategoryid());

                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        tblProducts.updateUI();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            int selectedRow = tblProducts.getSelectedRow();
            if (selectedRow < 0 || deleteProduct == false) {
                JOptionPane.showMessageDialog(this, "Choose the Row to Delete");
                return;
            }
            int r = JOptionPane.showConfirmDialog(this, "Do you want to Delete this Products?");
            if (r == JOptionPane.YES_OPTION) {
                Products_SE140736 pro = modelPro.getModelProducts().getList().get(selectedRow);
                if (modelPro.Delete(pro) == SUCCESS) { //xóa dlieu trong database trc
                    deleteProduct = false;
                    modelPro.getModelProducts().getList().remove(pro);
                    JOptionPane.showMessageDialog(this, "Deleted");
                    clearInformationProductsOnTable();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        tblProducts.updateUI();
    }//GEN-LAST:event_btnDeleteActionPerformed

